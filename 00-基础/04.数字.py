number = 3
print(number)

# 幂运算
print(number ** 2)


# 但需要注意的是，结果包含的小数位数可能是不确定的：
number = 0.1
print(number)

# 浮点数运算
print(number + 0.2)

# 浮点数运算
print(number - 0.2)

# 字符串和数字的拼接 会报错
age = 23
# message = "Happy "+age+"rd Birthday!"
message = "Happy " + str(age) + "rd Birthday!"
print(message)

# 除法
print(3/2) # 1.5

# 整除
print(3//2) # 1

# 取余
print(3%2) # 1

print(2.3 + 2) # 4.3