# __init__.py 文件在 Python 中有以下几个重要作用：
# 1. 标识包（Package）
# __init__.py 文件的存在告诉 Python 解释器这个目录是一个 Python 包
# 没有这个文件，Python 不会将该目录识别为包，无法进行导入操作
# 2. 包的初始化
# 当包被导入时，__init__.py 文件会被自动执行
# 可以在这里进行包的初始化工作，比如设置包级别的变量、导入子模块等
# 3. 控制包的导入行为
# 可以定义 __all__ 变量来控制 from package import * 时导入哪些模块
# 可以重新导出子模块，简化导入路径
# 4. 在你的 Django 项目中
# 从你的项目结构来看，hello_world/__init__.py 是 Django 项目的主包文件。目前它只是打印了一条消息，这在开发过程中可能用于调试。